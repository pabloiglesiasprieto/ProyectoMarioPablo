package git;

public class Pablo {

	public static int suma(int num1, int num2) {
		return num1+num2;
		
	}
	public static int resta(int num1, int num2) {
		return num1-num2;
		
	}
	public static int multiplicacion(int num1, int num2) {
		return num1*num2;
		
	}
	public static double division(int num1, int num2) {
		return num1/num2;
		
	}
}
